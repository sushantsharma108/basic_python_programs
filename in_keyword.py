# the "in" keyword can be used to check that whether a character 
# is present in a string or not. It is used with "if" condition, in for loops,data structures, etc...

name = "Sushant"
if 's' in name:             # after the "in" keyword we can place a var_name & a string also.
    print("s is present")   # for ex: in name / in "Sushant"
else:
    print("s isn't present")


