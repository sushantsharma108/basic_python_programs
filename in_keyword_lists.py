# # To check that whether an item is present in our list or not.
fruits = ['apple','banana','orange','grapes','kiwi','papaya']
if 'apple' in fruits:
    print("Apple is present")
else:
    print("Apple is not Present")

if 'mango' in fruits:
    print("Mango is present")
else:
    print("Mango is not Present")
