# Compare lists:
# we use == equality operator or is keyword for comparison
fruits1 = ['apple','banana','orange']
fruits2 = ['grapes','kiwi','orange','papaya']
fruits3 = ['apple','banana','orange']

print(fruits1 == fruits3)  # ""==" equality operator only checks if the values are same in the lists.
print(fruits1 is fruits3)   # "is" keyword checks that whether the items in lists are on the same place in memory or not.
#  inside the print function we can check the conditions.

